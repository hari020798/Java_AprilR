package com.java.rev;

public class Pattern_space {
	
	public static void main(String[] args) {
		
		for (char i=1; i<=5; i++) {
			
		
			for ( int j=1; j<i; j++) {
				System.out.print(" ");
			}
			
			for (int k=i; k<=5;k++) {
				System.out.print(k + " ");
			
		}
			System.out.println();

	}

}
}